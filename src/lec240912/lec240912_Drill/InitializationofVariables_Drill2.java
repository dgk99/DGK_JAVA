package lec240912_Drill;

public class InitializationofVariables_Drill2 {
    public static void main(String[] args) {
        // 지역변수 명시적 초기화
        int num = 10; // 명시적 초기화
        boolean flag = true; // 명시적 초기화
        char letter = 'A'; // 명시적 초기화

        System.out.println("정수 값: " + num);
        System.out.println("불리언 값: " + flag);
        System.out.println("문자 값: " + letter);
    }
}
