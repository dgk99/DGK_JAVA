package lec240912_Drill;

public class char_Drill {
    public static void main(String[] args) {
        char letterA = 'A'; // 문자 'A'
        char letterKorean = '가'; // 한글 문자 '가'
        char unicodeChar = '\u0041'; // 유니코드로 'A' 표현

        System.out.println("문자 A: " + letterA);
        System.out.println("한글 문자: " + letterKorean);
        System.out.println("유니코드 문자: " + unicodeChar);
    }
}
